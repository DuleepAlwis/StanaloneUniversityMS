    private void subjectLoad()
    {
        
        payment.setAnumber(anumber.getText());
        try 
        {
            
            ResultSet result = paymentcontrol.retrieveSubjects(payment,i);
            if(result!=null)
            {
                subjectable.setModel(DbUtils.resultSetToTableModel(result));
            }
            
        }
        catch (SQLException ex) {            
            Logger.getLogger(AddSubject.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Paymentform.class.getName()).log(Level.SEVERE, null, ex);
        }
        
            
    }
    

