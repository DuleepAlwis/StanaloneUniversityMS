    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        payment.setAnumber(anumber.getText());
        payment.setPaymentid(pid.getText());
        payment.setSemester(semester.getValue().toString());
        payment.setYear(year.getText());
        payment.setAmount(amount.getText());
        payment.setDate(date.getText());
        System.out.println(payment.getDate());
        try
        {
            
            if(paymentcontrol.addPayment(payment, i))
            {
                JOptionPane.showMessageDialog(rootPane,"Data Entered successfully");
            }
            else 
            {
                JOptionPane.showMessageDialog(rootPane,"Data Entered unsuccess");
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(rootPane,ex.getMessage());
            System.out.println(ex.getMessage());
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Paymentform.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane,ex.getMessage());
        }
        
    }//GEN-LAST:event_saveActionPerformed

