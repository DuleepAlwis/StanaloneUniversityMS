    private void btngradeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btngradeActionPerformed
        
        try
        {
            
            
            studentsubject.setAdmission(anumber.getText());
            studentsubject.setSubid(subject.getText());
            studentsubject.setAttempt(attempt.getValue().toString());
            studentsubject.setYear(year.getValue().toString());
            studentsubject.setSemester(semester.getValue().toString());
            String grade = studentsubcontrol.calculateGrades(studentsubject,i);
            txtgrade.setText(grade);
        }
        catch(SQLException ex)
        {
            System.out.println(ex.getMessage());
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CalculateGrade.class.getName()).log(Level.SEVERE, null, ex);
        }
           

    }//GEN-LAST:event_btngradeActionPerformed

    private void getstudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getstudentActionPerformed
        
        try {
            
            
                studentsubject.setSubid(subject.getText());
                studentsubject.setAttempt(attempt.getValue().toString());
                studentsubject.setYear(year.getValue().toString());
                studentsubject.setSemester(semester.getValue().toString());
                ResultSet result = studentsubcontrol.getAdmission(studentsubject, i);
                if(result!=null)
                {
                    student.setModel(DbUtils.resultSetToTableModel(result));
                    try
                    {
                        result.close();
                        
                    } catch (SQLException ex) {
                        System.out.println(ex.getMessage());
                    }
                }
               
                
                
            
        } catch (SQLException ex) {            
            Logger.getLogger(AddSubject.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane,ex.getMessage());
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CalculateGrade.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane,ex.getMessage());
        }
        
    }//GEN-LAST:event_getstudentActionPerformed

