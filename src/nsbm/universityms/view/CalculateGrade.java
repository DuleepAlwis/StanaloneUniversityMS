    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        
        studentsubject.setAdmission(anumber.getText());
        studentsubject.setSubid(subject.getText());
        studentsubject.setYear(year.getValue().toString());
        studentsubject.setSemester(semester.getValue().toString());
        studentsubject.setAttempt(attempt.getValue().toString());
        studentsubject.setGrade(txtgrade.getText());
        try
        {
            
            if(studentsubcontrol.gradeSave(studentsubject, i))
            {
                JOptionPane.showMessageDialog(rootPane,"Record entered successfully");
            }
            else 
            {
                JOptionPane.showMessageDialog(rootPane,"Record entered unsuccess");
            }
            
        }
        catch (SQLException ex) {
            Logger.getLogger(AddSubject.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane,ex.getMessage());
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CalculateGrade.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane,ex.getMessage());
        }
        
    }//GEN-LAST:event_saveActionPerformed

